/*
*   Handles
*       - current_graph => current graph object
*       - current_graph.vertices => array of Vertex2D objects for the vertices (this is what is saved/loaded as JSON)
*       - current_graph.ui.vertex_sprites => array of VertexSprite objects which can be drawn with current_graph.ui.drawVertexSprite(vs)
*       - current_graph.ui.edge_sprites => array of EdgeSprite objects which can be drawn with current_graph.ui.drawEdgeSprite(es)
*       - current_graph.ui.redrawGraph() => function which redraws entire graph
*/